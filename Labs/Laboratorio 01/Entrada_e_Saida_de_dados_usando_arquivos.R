#Entrada e saida de dados usando arquivos

#Comandos mais comuns para manipulação dos dados(entrada e saida)

scan- Lê um arquivo texto e transforma o conteudo em um vetor
      assumindo que cada elemento do arquivo esta separado 
      espaço ou por uma nova linha

read.csv- Lê um arquivo texto e transforma no formato csv e
          e transforma o conteudo em uma tabela do tipo dataframe

read.table - Lê um arquivo de texto em que cada elemento é separado
             por um determinado caracter separador e transforma o 
             conteuido em uma tabela do tipo dataframe.

write - comando simples de escrita de dados em arquivos

write.table - Escreve tabelas em um arquivo de saida, com varias opções
              de formatação

write.csv - Escreve tabelas em um arquivo de saida em formato CSV